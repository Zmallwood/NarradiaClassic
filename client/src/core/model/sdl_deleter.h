#pragma once
namespace Narradia
{
   ////////////////////////////////////////////////////////////////////////////////
   /// @brief Helper class for autmatically free SDL resources.
   ////////////////////////////////////////////////////////////////////////////////
   class SDLDeleter {
     public:
      ////////////////////////////////////////////////////////////////////////////////
      /// @brief Destroy window when no longer used.
      ///
      /// @param[in] win Window to destroy.
      ////////////////////////////////////////////////////////////////////////////////
      void operator()(SDL_Window *win);

      ////////////////////////////////////////////////////////////////////////////////
      /// @brief Destroy renderer when no longer used.
      ///
      /// @param[in] rend Renderer to destroy.
      ////////////////////////////////////////////////////////////////////////////////
      void operator()(SDL_Renderer *rend);

      ////////////////////////////////////////////////////////////////////////////////
      /// @brief Free surface when no longer used.
      ///
      /// @param[in] surf Surface to free.
      ////////////////////////////////////////////////////////////////////////////////
      void operator()(SDL_Surface *surf);

      ////////////////////////////////////////////////////////////////////////////////
      /// @brief Destroy texture when no longer used.
      ///
      /// @param[in] tex Texture to destroy.
      ////////////////////////////////////////////////////////////////////////////////
      void operator()(SDL_Texture *tex);

      ////////////////////////////////////////////////////////////////////////////////
      /// @brief Close font when no longer used.
      ///
      /// @param[in] font Font to close.
      ////////////////////////////////////////////////////////////////////////////////
      void operator()(TTF_Font *font);
   };
}
